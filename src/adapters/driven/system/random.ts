import type { Random } from '../../../core/ports/driven/Random.js';

export const mathRandom: Random = {
  next: () => Math.random(),
};

export function mulberry32(seed: number): Random {
  let s = seed >>> 0;
  return {
    next() {
      s = (s + 0x6d2b79f5) >>> 0;
      let t = s;
      t = Math.imul(t ^ (t >>> 15), t | 1);
      t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
      return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
    },
  };
}
